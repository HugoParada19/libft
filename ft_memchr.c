#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;

	str = (unsigned char *)s;
	while (n--)
		if (str[n] == (unsigned char)c)
			return ((void *)&str[n]);
	return (NULL);
}
